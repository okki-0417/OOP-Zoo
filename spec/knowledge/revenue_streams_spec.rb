# frozen_string_literal: true

require 'spec_helper'

# 収入源の知識（バックログ）。
#
# 中心の体験 = 「収支＝命のサバイバル経営シム」:
#   集客 → 収入 −(運営費＋飼育費＋再投資) → 収支
#     黒字 → 投資で拡大（同時にコスト↑・リスク↑） / 赤字 → 値上げ・売却・縮小で延命 / 破産 → ゲームオーバー
#
# ここでは入園料(ゲート収入)以外の収入の柱を並べる。
# 本文のない `it` は「まだ設計していない項目」＝pending=バックログ。
# 設計が固まったら本文(アサーション)を書いて実装を駆動する。
RSpec.describe '収入源' do
  describe '会員・年間パス' do
    it '会員(年間パス)は再来園を生み、来園者数の床を作ること'
    it '会員収入は通常入園料より単価は低いが、収入の季節変動を平準化すること'
  end

  describe '園内消費(物販・飲食・駐車場)' do
    it '来園者数に応じた二次消費(物販・飲食)の収入が生まれること'
    it '満足度(福祉・話題)の高い展示ほど客単価が上がること'
  end

  describe '寄付・遺贈' do
    it '評判が高いほど寄付が集まりやすいこと'
  end

  describe '助成金・補助金' do
    it '絶滅危惧種の繁殖など保全活動は助成金の対象になること'
  end

  describe 'スポンサー・動物オーナー制度' do
    it 'カリスマ性の高い種・個体にはスポンサー収入がつくこと'
  end

  describe '動物の売却・譲渡' do
    it '余剰個体を売却して資金化できること'
    it '売値は体格・希少度・健康状態で決まること'
    it '絶滅危惧種(CITES制限)は売却できないこと'
  end

  describe '特別イベント' do
    it '季節イベント(ナイトズー等)は一時的に集客と収入を押し上げること'
  end
end

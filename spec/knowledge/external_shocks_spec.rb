# frozen_string_literal: true

require 'spec_helper'

# 外的ショックの知識（バックログ）。
#
# 用語（経済ドメイン共通。ブレたらここに合わせる）:
#   集客 = f(魅力, 評判, 料金) … その日の来園者数(フロー)
#   評判 … 運営の質・信用(ストック)。福祉/死亡/スキャンダル/保全/清潔/過密で増減、放置で漸減
#   魅力 … 展示の引き「何が見られるか」。種カリスマ/多様性/話題(buzz=幼獣/新展示/イベント)
#   料金 … 入園料(需要レバー、弾力性)
#
# 内生圧力(a/b)の上に載せる"試練"の層。備え(準備金・収入や種の多様化)を怠った園を
# 選択的に苦しめる。先に内生の緊張があって初めて意味を持つので、実装順は後段。
# 本文のない `it` は未設計のバックログ(pending)。
RSpec.describe '外的ショック' do
  describe 'スキャンダル' do
    it '動物福祉スキャンダルは評判を暴落させること(評判を通じて集客が落ちる)'
  end

  describe '不況・パンデミック' do
    it '収入が落ちても飼育費(固定費)は止まらず、収支を強く圧迫すること'
    it '準備金のある園はショックを生き延びやすいこと'
  end

  describe '規制・罰金' do
    it '福祉基準違反は罰金の対象になること'
  end

  describe '疫病の発生' do
    it '疫病の発生は獣医費と休園による減収を招くこと'
  end
end

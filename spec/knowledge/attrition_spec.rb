# frozen_string_literal: true

require 'spec_helper'

RSpec.describe '生物ドメインの経済接続' do
  describe '見応えは種・展示レベルで決まる(個体の年齢ではない)' do
    it '見応えは個体の年齢ではなく、種のカリスマ性・話題で決まること'
    it '幼獣の誕生は一時的に見応え(話題)を押し上げること'
    it '隠れがち・不活発な個体は、展示としての見応え(引き)が弱いこと'
  end

  describe '資産・繁殖価値の減耗(個体レベル)' do
    it '高齢個体は繁殖価値(将来の子・血統・売却価値)が下がること'
    it '個体の死は資産を失わせ、最後の1頭ならその種の見応えを失うこと'
    it '近交は集客(見応え・評判)ではなく、個体群の繁殖価値・健全性を下げること'
  end

  describe '評判の動学' do
    it '評判は死亡だけでなく福祉・清潔・過密・保全でも増減すること'
  end

  describe '福祉の経済接続' do
    it '福祉を放置すると病気・死で資産(個体)を失うこと'
  end
end

# frozen_string_literal: true

require 'spec_helper'

# 生物ドメインの「経済接続」の知識（バックログ）。
#
# 重要な区別:
#   - 集客価値(attraction) … 「種・展示」レベルで決まる。種の魅力・幼獣の話題・
#     見栄え(福祉/活動)が源。個体の年齢はほぼ効かない（過剰モデリングを避ける）。
#   - 資産/繁殖価値(asset) … 「個体」レベル。年齢・健康・血統で決まり、繁殖・売却・
#     コストに効く。ここに老化・近交・死が効く。
#
# じわじわ効く(b)＝資産の減耗。放置すると常にマイナスへ効き、再投資の緊張を生む。
# 本文のない `it` は未設計のバックログ(pending)。
RSpec.describe '集客価値と資産の減耗(経済接続)' do
  describe '集客は種・展示レベルで決まる(個体の年齢ではない)' do
    it '集客価値は個体の年齢ではなく、種の魅力・幼獣の話題・展示の見栄え(福祉/活動)で決まること'
    it '幼獣は新奇性で一時的に強く集客すること'
    it '隠れている・活動の乏しい個体は、展示としての引きが弱いこと'
  end

  describe '資産・繁殖価値の減耗(個体レベル)' do
    it '高齢個体は繁殖価値(将来の子・血統・売却価値)が下がること'
    it '個体の死は資産を失わせ、最後の1頭ならその種ごと集客を失うこと'
    it '近交は集客ではなく、個体群の繁殖価値・健全性を下げること'
  end

  describe '評判の動学' do
    it '評判は良い出来事がないと徐々に下がること'
    it '評判は死亡だけでなく福祉・清潔・過密・保全でも増減すること'
  end

  describe '福祉負債の経済接続' do
    it '福祉を放置すると病気・死で資産(個体)を失うこと'
  end
end

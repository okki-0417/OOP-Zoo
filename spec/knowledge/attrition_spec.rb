# frozen_string_literal: true

require 'spec_helper'

# 生物ドメインの「経済接続」の知識（バックログ）。
#
# 経済ドメイン共通の用語・区別(集客=f(魅力,評判,料金), 二重計上回避 等)は economy_spec.rb 冒頭に集約。
#
# 本文のない `it` は未設計のバックログ(pending)。
RSpec.describe '生物ドメインの経済接続' do
  describe '魅力は種・展示レベルで決まる(個体の年齢ではない)' do
    it '魅力は個体の年齢ではなく、種のカリスマ性・話題で決まること'
    it '幼獣の誕生は一時的に魅力(話題)を押し上げること'
    it '隠れがち・不活発な個体は、展示としての魅力(引き)が弱いこと'
  end

  describe '資産・繁殖価値の減耗(個体レベル)' do
    it '高齢個体は繁殖価値(将来の子・血統・売却価値)が下がること'
    it '個体の死は資産を失わせ、最後の1頭ならその種の魅力を失うこと'
    it '近交は集客(魅力・評判)ではなく、個体群の繁殖価値・健全性を下げること'
  end

  describe '評判の動学' do
    # 自然減衰(良い出来事がないと下がる)は reputation_spec.rb「自然減衰」に実装済み。
    it '評判は死亡だけでなく福祉・清潔・過密・保全でも増減すること'
  end

  describe '福祉の経済接続' do
    # 福祉→評判 の経路は「評判の動学」で言う。ここは別経路(福祉→健康→死→資産喪失)。
    it '福祉を放置すると病気・死で資産(個体)を失うこと'
  end
end
